# Definition for a binary tree node.
class TreeNode:
    def __init__(self, x):
        self.val = x
        self.left = None
        self.right = None

class Solution:
    def levelOrderBottom(self, root: TreeNode) -> List[List[int]]:
        tmp = []
        res = []
        if root:
            tmp.append(root)
            while tmp:
                x = tmp[:]
                y = []
                tmp = []
                while x:
                    z = x.pop(0)
                    y.append(z.val)
                    if z.left:
                        tmp.append(z.left)
                    if z.right:
                        tmp.append(z.right)
                res.append(y)
        return res[::-1]
