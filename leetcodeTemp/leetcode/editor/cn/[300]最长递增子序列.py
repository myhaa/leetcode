class Solution:
    def lengthOfLIS(self, nums: List[int]) -> int:
        if not nums:
            return 0
        dp = []
        for i in nums:
            if not dp or dp[-1] < i:
                dp.append(i)
            else:
                l, r = 0, len(dp) - 1
                loc = r
                while l <= r:
                    mid = (l+r) >> 1
                    if dp[mid] >= i:
                        r = mid - 1
                        loc = mid
                    else:
                        l = mid + 1
                dp[loc] = i
        return len(dp)
        
